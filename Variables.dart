
// Variables y tipos de datos

void main() {

  // Entero (int)
  int edad = 19;

  // Flotante (double)
  double altura = 1.56;

  // Cadena de texto (String)
  String nombre = "Lisa";

  // Booleano (bool)
  bool esEstudiante = true;

  // Dinámico (dynamic)
  dynamic variableDinamica = "Este es un string";
  variableDinamica = 42; // Ahora es un número

  // Imprimir las variables

  print("Valores de la variables:\n");
  print("Edad: $edad");
  print("Altura: $altura");
  print("Nombre: $nombre");
  print("Es estudiante: $esEstudiante");
  print("Variable dinámica: $variableDinamica");
}